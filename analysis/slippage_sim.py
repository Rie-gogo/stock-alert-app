#!/usr/bin/env python3
"""
スリッページ考慮シミュレーション
今日（2026-06-11）の再現シミュレーション結果（+965,545円）に対して
現実的なスリッページを適用した場合の損益を試算する

スリッページ設定（片道）:
- 高流動性銘柄（8035東京エレクトロン、8306三菱UFJ、8316三井住友、7203トヨタ、9984ソフトバンクG）: 0.05%
- 中流動性銘柄（6920レーザーテック、6857アドバンテスト、6758ソニー、6981村田、6723ルネサス、9107川崎汽船、7011三菱重工）: 0.10%
- 低流動性銘柄（285A キオクシア、3436 SUMCO、3778さくら、4568第一三共、5016JX金属、5803フジクラ、6526ソシオネクスト、6976太陽誘電）: 0.20%
"""

# 今日の取引データ（銘柄コード、件数、損益、株価帯、ロット）
# 取引ログから手動で集計
trades = [
    # (銘柄コード, 銘柄名, 決済件数, 損益円, 平均株価, 平均ロット, 流動性)
    # 流動性: H=高, M=中, L=低
    ("285A",  "キオクシアHD",           8,  311394,  4200, 600, "L"),
    ("3436",  "SUMCO",                  11, 140079,  2600, 1000, "L"),
    ("3778",  "さくらインターネット",    4,  -62356,  3300, 800, "L"),
    ("4568",  "第一三共",               2,  -17818,  4100, 600, "L"),
    ("5016",  "JX金属",                 5,   25899,  2600, 1000, "L"),
    ("5803",  "フジクラ",               7,   30809,  3700, 700, "L"),
    ("6526",  "ソシオネクスト",         7,   56687,  3400, 800, "L"),
    ("6723",  "ルネサスエレクトロニクス",8,  24209,  2500, 1100, "M"),
    ("6758",  "ソニーグループ",          4,  -40912, 2700, 1000, "M"),
    ("6857",  "アドバンテスト",          5,   21960,  8900, 300, "M"),
    ("6920",  "レーザーテック",          8,   92365, 40000, 100, "M"),
    ("6976",  "太陽誘電",               20,  88476,  2500, 1100, "L"),
    ("6981",  "村田製作所",             10,  80163,  2700, 1000, "M"),
    ("7011",  "三菱重工業",              2,   2122,  6200, 400, "M"),
    ("7203",  "トヨタ自動車",            1,  -12600,  2700, 900, "H"),
    ("8035",  "東京エレクトロン",        5,   96291, 62000, 100, "H"),
    ("8306",  "三菱UFJ FG",             2,    2674,  3100, 800, "H"),
    ("8316",  "三井住友FG",              1,   22000,  6200, 400, "H"),
    ("9107",  "川崎汽船",               2,   41435,  2650, 1000, "M"),
    ("9984",  "ソフトバンクグループ",    7,   62666,  6400, 400, "H"),
]

# スリッページ率（片道）
SLIP_RATE = {
    "H": 0.0005,  # 0.05%
    "M": 0.0010,  # 0.10%
    "L": 0.0020,  # 0.20%
}

# 手数料（SBI証券アクティブプラン: 100万円超〜200万円以下 = 1,238円/日）
# 1日の取引代金合計を計算して手数料を算出
# 実際にはアクティブプランで1日の約定代金合計に応じた手数料
# 300万円×20銘柄=6,000万円の元金規模では1日の手数料は数千円程度

print("=" * 60)
print("スリッページ考慮シミュレーション（2026-06-11）")
print("=" * 60)
print()

total_no_slip = sum(t[3] for t in trades)
print(f"スリッページなし損益（再現シミュレーション）: +{total_no_slip:,.0f}円")
print()

# スリッページ計算
# 各取引でエントリー時・エグジット時の両方にスリッページが発生
# エントリー: 買い→実際の約定価格は高め、売り（空売り）→実際の約定価格は低め
# エグジット: 売り→実際の約定価格は低め、買い戻し→実際の約定価格は高め
# = 往復で 2 × スリッページ率 × 取引代金 のコストが発生

print("銘柄別スリッページ試算:")
print(f"{'銘柄':<20} {'流動性':<4} {'決済件数':<8} {'損益（元）':>12} {'スリップ':>10} {'損益（後）':>12}")
print("-" * 72)

total_slip_cost = 0
results = []

for code, name, count, profit, avg_price, avg_lot, liquidity in trades:
    slip_rate = SLIP_RATE[liquidity]
    # 往復スリッページコスト = 2 × slip_rate × 平均株価 × 平均ロット × 決済件数
    slip_cost = 2 * slip_rate * avg_price * avg_lot * count
    adj_profit = profit - slip_cost
    total_slip_cost += slip_cost
    results.append((code, name, count, profit, slip_cost, adj_profit, liquidity))
    print(f"{name:<20} {liquidity:<4} {count:<8} {profit:>12,.0f} {-slip_cost:>10,.0f} {adj_profit:>12,.0f}")

print("-" * 72)
total_adj = total_no_slip - total_slip_cost
print(f"{'合計':<20} {'':4} {119:<8} {total_no_slip:>12,.0f} {-total_slip_cost:>10,.0f} {total_adj:>12,.0f}")

print()
print("=" * 60)
print("サマリー")
print("=" * 60)
print(f"スリッページなし損益: +{total_no_slip:,.0f}円")
print(f"スリッページコスト:   -{total_slip_cost:,.0f}円")
print(f"スリッページ後損益:   {'+' if total_adj >= 0 else ''}{total_adj:,.0f}円")
print(f"スリッページ影響率:   {total_slip_cost/total_no_slip*100:.1f}%削減")
print()

# 手数料試算（SBI証券アクティブプラン）
# 1日の約定代金合計
total_trade_value = sum(t[4] * t[5] * t[2] * 2 for t in trades)  # 往復
print(f"1日の約定代金合計（往復）: {total_trade_value/1e8:.2f}億円")

# アクティブプランの手数料（2024年現在）
# 〜100万円: 0円
# 〜200万円: 1,238円
# 〜300万円: 1,691円
# 〜500万円: 2,307円
# 〜1,000万円: 3,300円
# 〜2,000万円: 4,620円
# 〜3,000万円: 5,720円
# 3,000万円超: 5,720円（上限）
# ※ただし信用取引の場合は別途金利・貸株料が発生

# 今日の約定代金は数億円規模のため、アクティブプランの上限5,720円/日
commission = 5720  # 円/日（アクティブプラン上限）
print(f"手数料（アクティブプラン上限）: {commission:,}円/日")
print()

final_profit = total_adj - commission
print(f"最終損益（スリッページ+手数料後）: {'+' if final_profit >= 0 else ''}{final_profit:,.0f}円")
print()

# 現実的なシナリオ比較
print("=" * 60)
print("現実的シナリオ比較（スリッページ率の感度分析）")
print("=" * 60)
print(f"{'シナリオ':<30} {'スリップコスト':>14} {'損益':>12}")
print("-" * 58)

scenarios = [
    ("楽観（H:0.03%, M:0.07%, L:0.15%）", 0.0003, 0.0007, 0.0015),
    ("基準（H:0.05%, M:0.10%, L:0.20%）", 0.0005, 0.0010, 0.0020),
    ("悲観（H:0.10%, M:0.20%, L:0.40%）", 0.0010, 0.0020, 0.0040),
    ("最悪（H:0.15%, M:0.30%, L:0.60%）", 0.0015, 0.0030, 0.0060),
]

for scenario_name, h_rate, m_rate, l_rate in scenarios:
    rates = {"H": h_rate, "M": m_rate, "L": l_rate}
    slip = sum(2 * rates[t[6]] * t[4] * t[5] * t[2] for t in trades)
    profit = total_no_slip - slip - commission
    print(f"{scenario_name:<30} {-slip:>14,.0f} {'+' if profit >= 0 else ''}{profit:>12,.0f}")

print()
print("注: 全取引に「出来高薄」警告あり → 実際は「基準〜悲観」シナリオが現実的")
print()

# 日次換算（20営業日）
print("=" * 60)
print("20営業日換算（バックテスト比較）")
print("=" * 60)
print(f"バックテスト実績（5銘柄・損切り2.0%）: +453,615円 / 20日 = 日平均+22,681円")
print()
print("今日（20銘柄・損切り0.7%）の結果:")
print(f"  スリッページなし: +{total_no_slip:,.0f}円（1日）")
print(f"  基準シナリオ後:   {'+' if total_adj >= 0 else ''}{total_adj:,.0f}円（1日）")
print()
print("注意: 今日は上昇相場（日経平均+1.5%程度）のため、")
print("      通常日の期待値はこの50〜70%程度と見積もるべき")
