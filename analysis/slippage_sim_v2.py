SLIP_RATE_V2 = {
    "H": 0.0003,   # 0.03%（1〜2ティック）
    "M": 0.0006,   # 0.06%（2〜3ティック）
    "L": 0.0010,   # 0.10%（3〜5ティック）
}

# 旧スリッページ率（参考）
SLIP_RATE_V1 = {
    "H": 0.0005,
    "M": 0.0010,
    "L": 0.0020,
}

total_no_slip = sum(t[3] for t in trades)

print("=" * 65)
